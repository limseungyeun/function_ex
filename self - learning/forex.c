#include <stdio.h>

int main() {
    int a[5] = { 9, 1, 7, 3, 5 };
    int i, j, min, t;

    // 1. 기준 위치를 정하는 반복문 (마지막 칸은 알아서 정렬되므로 4번만 반복)
    for (i = 0; i < 4; i++) {
        min = i; // 일단 현재 기준 위치(i)를 가장 작은 값의 위치라고 가정함

        // 2. 기준 위치 다음부터 끝까지 훑으며 진짜 제일 작은 값을 찾음
        for (j = i + 1; j < 5; j++) {
            if (a[j] < a[min]) // 만약 더 작은 값을 발견하면
                min = j;       // 그 위치(인덱스)를 min에 저장
        }

        // 3. [자리 바꾸기] 기준 위치(i)의 값과 찾은 최솟값(min)의 자리를 서로 바꿈
        t = a[i];       // 컵 A의 물을 빈 컵 t에 잠시 옮김
        a[i] = a[min];  // 컵 B의 물을 컵 A에 부음
        a[min] = t;     // 빈 컵 t에 있던 물을 컵 B에 부음
    }

    // 4. 정렬된 결과 출력
    for (i = 0; i < 5; i++) {
        printf("%2d", a[i]);
    }

    return 0;
}